def upper_text(text):
    """Функция приводит верхнему регистру. И еще что-то.
    :param text: Текст для приведения
    :return: Текст в верхнем регистре
    """
    return text.upper()


def lower_text(text):
    """
    Функция приводит текст к нижнему регистру.
    :param text: Текст для приведения
    :return: Текст в нижнем регистре
    """
    return text.lower()
